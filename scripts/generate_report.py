#!/usr/bin/env python3
"""
generate_report.py - Generate markdown comparison report from results
"""

import argparse
import json
from datetime import datetime
from pathlib import Path


def generate_report(results_path: Path, output_path: Path):
    """Generate a markdown comparison report."""
    
    with open(results_path, 'r') as f:
        data = json.load(f)
    
    results = data.get('results', [])
    
    report = f"""# Memory Systems Benchmark Report

**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S UTC')}

## Summary

| Adapter | Accuracy | Latency (ms) | Recall | F1 Score |
|---------|----------|--------------|--------|----------|
"""
    
    for r in results:
        report += f"| {r['adapter']} | {r['accuracy']*100:.1f}% | {r['average_latency_ms']:.1f}ms | {r['recall']*100:.1f}% | {r['f1_score']:.3f} |\n"
    
    report += """
## Performance Tiers

| Tier | Accuracy | Latency | Description |
|------|----------|---------|-------------|
| 🥇 Gold | ≥ 90% | < 50ms | Production-ready, excellent |
| 🥈 Silver | ≥ 80% | < 100ms | Good for most use cases |
| 🥉 Bronze | ≥ 70% | < 200ms | Acceptable, room for improvement |
"""
    
    # Add ranking
    if results:
        report += f"""
## Rankings

### Best Accuracy
1. **{results[0]['adapter']}** ({results[0]['accuracy']*100:.1f}%)

### Fastest Response
"""
        fastest = min(results, key=lambda x: x['average_latency_ms'])
        report += f"1. **{fastest['adapter']}** ({fastest['average_latency_ms']:.1f}ms)\n"
    
    report += """
---
*Report generated by LoCoMo Benchmark Suite*
"""
    
    with open(output_path, 'w') as f:
        f.write(report)
    
    print(f"Report generated: {output_path}")


def main():
    parser = argparse.ArgumentParser(description='Generate benchmark report')
    parser.add_argument('--results', '-r', required=True, help='Path to summary JSON')
    parser.add_argument('--output', '-o', required=True, help='Output markdown path')
    
    args = parser.parse_args()
    
    generate_report(Path(args.results), Path(args.output))


if __name__ == '__main__':
    main()
