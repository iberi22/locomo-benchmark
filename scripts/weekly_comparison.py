#!/usr/bin/env python3
"""
weekly_comparison.py - Generate weekly comparison report
"""

import argparse
import json
from datetime import datetime
from pathlib import Path


def generate_weekly_comparison(
    trends_path: Path,
    output_path: Path,
    from_date: str,
    to_date: str
):
    """Generate weekly comparison report."""
    
    with open(trends_path, 'r') as f:
        trends = json.load(f)
    
    report = f"""# Weekly Memory Systems Comparison

**Period:** {from_date} to {to_date}
**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S UTC')}

## Performance Changes

| System | Trend | Accuracy Change | Latency Change |
|--------|-------|----------------|---------------|
"""
    
    for adapter, data in trends.get('adapters', {}).items():
        trend_emoji = "📈" if data['trend'] == 'improving' else "➡️" if data['trend'] == 'stable' else "📉"
        acc_sign = "+" if data['accuracy_delta'] >= 0 else ""
        lat_sign = "-" if data['latency_delta'] <= 0 else "+"
        
        report += f"| {adapter} | {trend_emoji} {data['trend']} | {acc_sign}{data['accuracy_delta']*100:.1f}% | {lat_sign}{abs(data['latency_delta']):.1f}ms |\n"
    
    report += """
## Recommendations

"""
    
    # Generate recommendations based on trends
    for adapter, data in trends.get('adapters', {}).items():
        if data['accuracy_delta'] < 0:
            report += f"- **{adapter}**: Accuracy dropped, needs investigation\n"
        elif data['latency_delta'] > 10:
            report += f"- **{adapter}**: Latency increased significantly\n"
    
    report += """
## Action Items

- [ ] Review underperforming systems
- [ ] Schedule performance optimization sprint
- [ ] Update benchmark infrastructure if needed

---
*Weekly report generated by LoCoMo Benchmark Suite*
"""
    
    with open(output_path, 'w') as f:
        f.write(report)
    
    print(f"Weekly comparison saved to {output_path}")


def main():
    parser = argparse.ArgumentParser(description='Generate weekly comparison report')
    parser.add_argument('--trends', '-t', required=True, help='Trends JSON path')
    parser.add_argument('--output', '-o', required=True, help='Output markdown path')
    parser.add_argument('--from-date', default='30 days ago', help='Start date')
    parser.add_argument('--to-date', default='today', help='End date')
    
    args = parser.parse_args()
    
    generate_weekly_comparison(
        Path(args.trends),
        Path(args.output),
        args.from_date,
        args.to_date
    )


if __name__ == '__main__':
    main()
